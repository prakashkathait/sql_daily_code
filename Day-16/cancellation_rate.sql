select t.request_at,
COUNT(CASE WHEN t.status IN ('cancelled_by_client','cancelled_by_driver') THEN 1 else NULL END) AS tot_cancelled,
COUNT(1) AS tot_trips,
round((COUNT(CASE WHEN t.status IN ('cancelled_by_client','cancelled_by_driver') THEN 1 else NULL END) :: decimal 
												/ COUNT(1) :: decimal) *100,2)AS cancellation_rate								
from trips t 
JOIN users u on t.client_id = u.users_id 
JOIN users u2 on  t.driver_id = u2.users_id
WHERE u.banned = 'No' and u2.banned ='No'
GROUP BY t.request_at;





